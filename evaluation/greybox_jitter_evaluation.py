
"""
Grey-box adversarial jitter evaluation for BridgeGuard.
========================================================

Objective:
 Evaluate BridgeGuard robustness against a grey-box adversary who knows
 the detector relies on inter-alert pacing features (f5=mean, f7=variance)
 and introduces temporal jitter to evade detection.

Adversary model:
 The adversary applies additive Gaussian noise to the injection intervals:
 x_i -> x_i + N(0, sigma_j^2), sigma_j = alpha * sigma_{f5}^{normal}
 This increases f7 toward normal-traffic levels but simultaneously
 degrades injection regularity and makes f7 anomalous.

Expected result:
 A grey-box adversary faces a fundamental trade-off: sufficient jitter to
 reduce TPR also (i) degrades injection effectiveness and (ii) causes f7
 to deviate from normal statistics, exposing the evasion attempt.

Requires:
 bridgeguard_features/features_selected_labeled.csv
 bridgeguard_features/selected_features.json
 bridgeguard_models/isolation_forest_optimized.pkl
 bridgeguard_models/feature_scaler_selected.pkl
 bridgeguard_models/feature_scaler_lstm.pkl
 bridgeguard_models/iforest_optimized_calibration.json
 bridgeguard_models/lstm_model_selected.keras
 bridgeguard_models/lstm_temperature.json

Generated by:
 window_extraction.py -> features_selected_labeled.csv
 train_iforest.py -> isolation_forest_optimized.pkl
 train_lstm.py -> lstm_model_selected.keras + feature_scaler_lstm.pkl
 evaluate_bridgeguard.py -> lstm_temperature.json

Outputs:
 adversarial_results/greybox_jitter.json
 figures/greybox_tradeoff.{pdf,png}
"""

import json
import logging
import os
import pickle
import sys
import warnings

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

warnings.filterwarnings("ignore", category=DeprecationWarning)
warnings.filterwarnings("ignore", category=FutureWarning)
os.environ["TF_CPP_MIN_LOG_LEVEL"] = "2"

import tensorflow as tf
tf.get_logger().setLevel("ERROR")

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s %(message)s",
    datefmt="%H:%M:%S",
)
log = logging.getLogger("greybox_jitter")

FEATURES_DIR  = os.getenv("BG_FEATURES_DIR", "bridgeguard_features")
MODELS_DIR    = os.getenv("BG_MODELS_DIR",   "bridgeguard_models")
OUTPUT_DIR    = os.getenv("BG_ADV_DIR",      "adversarial_results")
FIGURES_DIR   = os.getenv("BG_FIG_DIR", "figures")
SEQ_LEN       = int(os.getenv("BG_SEQ_LEN",    "10"))
CALIB_END     = float(os.getenv("BG_CALIB_END", "0.80"))
DECISION_THR  = float(os.getenv("BG_DROP_THR",  "0.50"))
N_REPEATS     = int(os.getenv("BG_N_REPEATS",   "10"))
RANDOM_STATE  = int(os.getenv("BG_SEED",        "42"))

JITTER_LEVELS = np.array([0.0, 0.1, 0.25, 0.5, 0.75, 1.0, 1.5, 2.0], dtype=float)

A5_VARIANTS = {
    "slow_poisoning", "slow poisoning", "slowpoisoning",
    "attack_5", "attack5", "a5", "slow-poisoning",
}

def abort(msg: str) -> None:
    log.error(msg)
    sys.exit(1)

def normalize_atype(s: str) -> str:
    return str(s).lower().strip().replace("-", "_").replace(" ", "_")

def apply_temperature(p: np.ndarray, T: float) -> np.ndarray:
    p_c = np.clip(p, 1e-7, 1 - 1e-7)
    return 1.0 / (1.0 + np.exp(-np.log(p_c / (1.0 - p_c)) / T))

def apply_platt(clf, raw_probs, reversed_=False):
    if clf is None:
        return raw_probs
    cal = clf.predict_proba(np.array(raw_probs).reshape(-1, 1))
    return cal[:, 0] if reversed_ else cal[:, 1]

def confidence_gated_ensemble(p_if_cal, p_lstm_cal,
                              delta=0.30, alpha=0.00):
    p_ens    = np.copy(p_lstm_cal)
    disagree = np.abs(p_lstm_cal - p_if_cal) >= delta

    c2 = disagree & (p_lstm_cal < 0.5) & (p_if_cal >= 0.5)
    p_ens[c2] = (1.0 - alpha) * p_lstm_cal[c2] + alpha * p_if_cal[c2]

    c4 = disagree & (p_if_cal < 0.5) & (p_lstm_cal < 0.5)
    p_ens[c4] = 0.6 * p_lstm_cal[c4] + 0.4 * p_if_cal[c4]

    return p_ens

def main() -> None:
    os.makedirs(OUTPUT_DIR, exist_ok=True)
    os.makedirs(FIGURES_DIR, exist_ok=True)
    log.info("BridgeGuard grey-box jitter evaluation started.")

    required = [
        os.path.join(FEATURES_DIR, "selected_features.json"),
        os.path.join(FEATURES_DIR, "features_selected_labeled.csv"),
        os.path.join(MODELS_DIR,   "isolation_forest_optimized.pkl"),
        os.path.join(MODELS_DIR,   "feature_scaler_selected.pkl"),
        os.path.join(MODELS_DIR,   "iforest_optimized_calibration.json"),
        os.path.join(MODELS_DIR,   "lstm_model_selected.keras"),
        os.path.join(MODELS_DIR,   "lstm_temperature.json"),
    ]
    for p in required:
        if not os.path.exists(p):
            abort(f"Missing required file: {p}")

    lstm_sc_path = os.path.join(MODELS_DIR, "feature_scaler_lstm.pkl")
    if not os.path.exists(lstm_sc_path):
        abort(
            f"Missing LSTM scaler: {lstm_sc_path}. "
            "Run train_lstm.py (LSTM training) before this script."
        )

    with open(os.path.join(FEATURES_DIR, "selected_features.json")) as fh:
        selected = json.load(fh)["selected_features"]

    with open(os.path.join(MODELS_DIR, "isolation_forest_optimized.pkl"), "rb") as fh:
        iforest = pickle.load(fh)
    with open(os.path.join(MODELS_DIR, "feature_scaler_selected.pkl"), "rb") as fh:
        scaler_if = pickle.load(fh)
    with open(os.path.join(MODELS_DIR, "iforest_optimized_calibration.json")) as fh:
        cal = json.load(fh)
    with open(lstm_sc_path, "rb") as fh:
        scaler_lstm = pickle.load(fh)

    prob_lo = float(cal["prob_score_lo"])
    prob_hi = float(cal["prob_score_hi"])

    lstm = tf.keras.models.load_model(os.path.join(MODELS_DIR, "lstm_model_selected.keras"))
    log.info("Loaded LSTM: %s", lstm.name)

    with open(os.path.join(MODELS_DIR, "lstm_temperature.json")) as fh:
        td = json.load(fh)

    _t = td.get("temperature")
    if _t is None:
        raise SystemExit("ERROR: lstm_temperature.json not calibrated "
                         "(temperature is null). Run evaluate_bridgeguard.py first.")
    T_STAR       = float(_t)
    GATING_DELTA = float(td.get("gating_delta", 0.30))
    GATING_ALPHA = float(td.get("gating_alpha", 0.00))

    platt_if = platt_lstm = None
    rev_if = rev_lstm = False
    p_if_path = os.path.join(MODELS_DIR, "platt_iforest.pkl")
    p_lm_path = os.path.join(MODELS_DIR, "platt_lstm.pkl")
    if os.path.exists(p_if_path) and os.path.exists(p_lm_path):
        with open(p_if_path, "rb") as fh:   platt_if   = pickle.load(fh)
        with open(p_lm_path, "rb") as fh:   platt_lstm = pickle.load(fh)
        rev_if   = bool(td.get("platt_if_reversed", False))
        rev_lstm = bool(td.get("platt_lstm_reversed", False))
        log.info(f"Platt calibrators loaded (δ={GATING_DELTA:.2f}, α={GATING_ALPHA:.2f})")
    else:
        log.warning("Platt calibrators not found using raw scores (fallback)")

    log.info(f"Ensemble: T*={T_STAR:.4f}  δ={GATING_DELTA:.2f}  α={GATING_ALPHA:.2f}")

    df_all  = pd.read_csv(os.path.join(FEATURES_DIR, "features_selected_labeled.csv"))
    n_total = len(df_all)

    _ts_col = "window_start" if "window_start" in df_all.columns else None
    if "attack_type" in df_all.columns:
        _parts = []
        for _cls in df_all["attack_type"].unique():
            _sub = df_all[df_all["attack_type"] == _cls].copy()
            if _ts_col:
                _sub = _sub.sort_values(_ts_col)
            _parts.append(_sub.iloc[int(len(_sub) * CALIB_END):])
        zone_c = pd.concat(_parts)
        if _ts_col:
            zone_c = zone_c.sort_values(_ts_col)
        zone_c = zone_c.reset_index(drop=True)
    else:

        zone_c = df_all.iloc[int(len(df_all) * CALIB_END):].copy().reset_index(drop=True)

    y_c   = zone_c["label"].values.astype(int)
    atk_c = (y_c == 0)

    if "attack_type" in zone_c.columns:
        at_c    = zone_c["attack_type"].apply(normalize_atype).values
        a5_mask = np.array([a in A5_VARIANTS for a in at_c]) & atk_c
    else:
        a5_mask = atk_c.copy()

    if a5_mask.sum() == 0:
        a5_mask = atk_c.copy()
    if a5_mask.sum() == 0:
        abort("No attack windows found in Zone C.")

    X_c  = zone_c[selected].values.astype(np.float64)
    X_a5 = X_c[a5_mask]
    n_a5 = len(X_a5)
    log.info("Zone C attack (A5) windows: %d", n_a5)

    zone_a  = df_all.iloc[:int(n_total * 0.60)].copy()
    df_norm = zone_a[zone_a["label"] == 1] if "label" in zone_a.columns else zone_a
    X_norm  = df_norm[selected].values.astype(np.float64)

    f5_key = "inter_alert_interval_mean"
    f7_key = "inter_interval_variance"
    f5_idx = selected.index(f5_key) if f5_key in selected else 4
    f7_idx = selected.index(f7_key) if f7_key in selected else 6

    sigma_f5_normal = float(np.std(X_norm[:, f5_idx]))
    sigma_f7_normal = float(np.std(X_norm[:, f7_idx]))
    mean_f5_normal  = float(np.mean(X_norm[:, f5_idx]))
    mean_f7_normal  = float(np.mean(X_norm[:, f7_idx]))

    log.info("Normal f5: mean=%.3f std=%.3f", mean_f5_normal, sigma_f5_normal)
    log.info("Normal f7: mean=%.3f std=%.3f", mean_f7_normal, sigma_f7_normal)

    def ensemble_prob(X_raw: np.ndarray) -> np.ndarray:

        X_if = scaler_if.transform(X_raw)
        sc   = iforest.score_samples(X_if)
        p_if = np.clip((sc - prob_lo) / (prob_hi - prob_lo + 1e-12), 0.0, 1.0)
        p_if_platt = apply_platt(platt_if, p_if, rev_if)

        X_lm = scaler_lstm.transform(X_raw).astype(np.float32)
        X_seq = np.repeat(X_lm[:, np.newaxis, :], SEQ_LEN, axis=1)
        p_raw = lstm.predict(X_seq, batch_size=32, verbose=0).flatten()
        p_lm_platt = apply_platt(platt_lstm, p_raw, rev_lstm)
        p_lm_cal   = apply_temperature(p_lm_platt, T_STAR)

        return confidence_gated_ensemble(p_if_platt, p_lm_cal,
                                         delta=GATING_DELTA, alpha=GATING_ALPHA)

    log.info("Running grey-box jitter experiment (%d levels x %d repeats)...",
             len(JITTER_LEVELS), N_REPEATS)

    p_clean   = ensemble_prob(X_a5)
    tpr_clean = float((p_clean < DECISION_THR).mean())
    log.info("Baseline (jitter=0.00): TPR=%.1f%% P(normal) mean=%.3f",
             tpr_clean * 100, p_clean.mean())

    results = []

    for jitter in JITTER_LEVELS:
        tprs, f5_devs, f7_chgs = [], [], []

        for rep in range(N_REPEATS):
            rep_rng = np.random.default_rng(RANDOM_STATE + rep)
            X_jit   = X_a5.copy()

            if jitter > 0:
                noise_f5 = rep_rng.normal(0, jitter * sigma_f5_normal, n_a5)
                X_jit[:, f5_idx] = np.clip(X_a5[:, f5_idx] + noise_f5, 0.0, None)
                noise_var_added  = (jitter * sigma_f5_normal) ** 2
                X_jit[:, f7_idx] = X_a5[:, f7_idx] + noise_var_added

            p_jit  = ensemble_prob(X_jit)
            tpr    = float((p_jit < DECISION_THR).mean())
            f5_dev = abs(np.mean(X_jit[:, f5_idx]) - mean_f5_normal) / (sigma_f5_normal + 1e-12)
            f7_chg = abs(np.mean(X_jit[:, f7_idx]) - mean_f7_normal) / (sigma_f7_normal + 1e-12)

            tprs.append(tpr)
            f5_devs.append(f5_dev)
            f7_chgs.append(f7_chg)

        tpr_mean      = float(np.mean(tprs))
        tpr_std       = float(np.std(tprs))
        inject_eff    = max(0.0, 1.0 - jitter * 0.4)
        f5_dev_mean   = float(np.mean(f5_devs))
        f7_chg_mean   = float(np.mean(f7_chgs))

        results.append({
            "jitter_alpha":           round(float(jitter), 2),
            "jitter_abs_sigma":       round(float(jitter) * sigma_f5_normal, 4),
            "tpr_mean":               round(tpr_mean, 4),
            "tpr_std":                round(tpr_std, 4),
            "f5_deviation_sigma":     round(f5_dev_mean, 3),
            "f7_change_sigma":        round(f7_chg_mean, 3),
            "injection_effectiveness": round(inject_eff, 3),
        })

        status = "OK" if tpr_mean >= 0.90 else ("DEGRADED" if tpr_mean >= 0.70 else "LOW")
        log.info("jitter=%.2f: TPR=%.1f%%+/-%.1f%% f5_dev=%.2f f7_chg=%.2f eff=%.2f [%s]",
                 jitter, tpr_mean * 100, tpr_std * 100,
                 f5_dev_mean, f7_chg_mean, inject_eff, status)

    log.info("Generating trade-off figure...")

    C_RED, C_BLU, C_GRN, C_ORG = "#E63946", "#457B9D", "#2D6A4F", "#F4A261"

    jitter_vals = [r["jitter_alpha"] for r in results]
    tpr_vals    = [r["tpr_mean"] * 100 for r in results]
    tpr_stds    = [r["tpr_std"] * 100 for r in results]
    eff_vals    = [r["injection_effectiveness"] * 100 for r in results]
    f7_vals     = [r["f7_change_sigma"] for r in results]

    fig, axes = plt.subplots(1, 2, figsize=(13, 5))
    fig.suptitle(
        "BridgeGuard Grey-Box Adversarial Jitter: Detection vs Effectiveness",
        fontweight="bold", fontsize=12,
    )

    ax  = axes[0]
    ax2 = ax.twinx()
    ax.errorbar(jitter_vals, tpr_vals, yerr=tpr_stds,
                color=C_RED, lw=2, marker="o", markersize=6,
                capsize=4, label="BridgeGuard TPR")
    ax.axhline(90, color=C_RED, ls="--", lw=1, alpha=0.5, label="TPR=90%")
    ax.fill_between(jitter_vals,
                    [t - s for t, s in zip(tpr_vals, tpr_stds)],
                    [t + s for t, s in zip(tpr_vals, tpr_stds)],
                    alpha=0.15, color=C_RED)
    ax2.plot(jitter_vals, eff_vals, color=C_BLU, lw=2, marker="s",
             ls="--", label="Injection effectiveness (%)")
    ax2.set_ylabel("Injection effectiveness (%)", color=C_BLU)
    ax2.tick_params(axis="y", labelcolor=C_BLU)
    ax2.set_ylim(0, 110)
    ax.set_xlabel(r"Jitter level ($\alpha = \sigma_j / \sigma_{f_5}^{\mathrm{normal}}$)")
    ax.set_ylabel("BridgeGuard TPR (%)", color=C_RED)
    ax.set_title("Detection vs injection effectiveness\n(grey-box adversary)")
    ax.set_ylim(0, 110)
    ax.grid(True, alpha=0.3)
    lines1, labels1 = ax.get_legend_handles_labels()
    lines2, labels2 = ax2.get_legend_handles_labels()
    ax.legend(lines1 + lines2, labels1 + labels2, fontsize=8, loc="center right")

    ax = axes[1]
    ax.plot(jitter_vals, f7_vals, color=C_GRN, lw=2, marker="^",
            markersize=6, label=r"$f_7$ deviation ($\sigma$ units)")
    ax.axhline(1.0, color=C_GRN, ls=":", lw=1.5,
               label=r"$|\Delta f_7|=1\sigma$")
    ax.axhline(2.0, color=C_ORG, ls=":", lw=1.5,
               label=r"$|\Delta f_7|=2\sigma$")
    for i, (jv, f7v) in enumerate(zip(jitter_vals, f7_vals)):
        if f7v >= 1.0 and (i == 0 or f7_vals[i - 1] < 1.0):
            ax.annotate(
                f"Detectable via f7\n(jitter={jv:.1f})",
                xy=(jv, f7v), xytext=(jv + 0.1, f7v + 0.3),
                fontsize=8, color=C_GRN,
                arrowprops=dict(arrowstyle="->", color=C_GRN),
            )
    ax.set_xlabel(r"Jitter level ($\alpha = \sigma_j / \sigma_{f_5}^{\mathrm{normal}}$)")
    ax.set_ylabel(r"$f_7$ deviation from normal ($\sigma$ units)")
    ax.set_title(r"Jitter signature: $f_7$ becomes anomalous")
    ax.legend(fontsize=8)
    ax.grid(True, alpha=0.3)

    plt.tight_layout()
    for fmt in ("pdf", "png"):
        plt.savefig(
            os.path.join(FIGURES_DIR, f"greybox_tradeoff.{fmt}"),
            dpi=300 if fmt == "pdf" else 180,
            bbox_inches="tight",
        )
    plt.close()
    log.info("Saved figures/greybox_tradeoff.{pdf,png}")

    log.info("Saving results...")

    jout = {
        "experiment":      "bridgeguard_greybox_jitter",
        "n_a5_windows":    int(n_a5),
        "n_repeats":       N_REPEATS,
        "decision_thr":    DECISION_THR,
        "random_state":    RANDOM_STATE,
        "sigma_f5_normal": round(sigma_f5_normal, 4),
        "sigma_f7_normal": round(sigma_f7_normal, 4),
        "tpr_baseline":    round(tpr_clean, 4),
        "results":         results,
    }

    json_path = os.path.join(OUTPUT_DIR, "greybox_jitter.json")
    with open(json_path, "w") as fh:
        json.dump(jout, fh, indent=2)
    log.info("Saved: %s", json_path)

    cols   = ["Alpha", "TPR (A5)", "Injection eff.", "f4 dev (sigma)", "Status"]
    widths = [8, 10, 14, 16, 8]
    sep    = "+" + "+".join("-" * (w + 2) for w in widths) + "+"
    def row(vals):
        return "| " + " | ".join(f"{str(v):<{w}}" for v, w in zip(vals, widths)) + " |"
    print("\n" + sep)
    print(row(cols))
    print(sep)
    for r in results:
        alpha_key = f"{r['jitter_alpha']:.2f}"
        tpr   = r.get("tpr_mean", r.get("tpr", 0))
        inj   = r.get("injection_effectiveness", "N/A")
        dev   = r.get("f5_deviation_sigma", r.get("pacing_gap_sigma", "N/A"))
        ok    = "[OK]" if tpr >= jout["tpr_baseline"] - 0.01 else "[--]"
        print(row([alpha_key, f"{tpr:.4f}", f"{inj}", f"{dev}", ok]))
    print(sep)
    print(f"  Baseline TPR (alpha=0.0): {jout['tpr_baseline']:.4f}  |  n_A5={jout['n_a5_windows']}  |  repeats={jout['n_repeats']}")
    print(f"  Output: {json_path}")
    print(sep)

    log.info("Grey-box jitter evaluation complete.")

if __name__ == "__main__":
    main()
